package www.xwcms.net.bean;

public class Message {

	private String messageId;
	private String messageName;
	private String messageSex;
	private String messageEmail;
	private String messageQQ;
	private String messageTel;
	private String messageAdd;
	private String messageCont;
	private String messageTime;
	
	public String getMessageTime() {
		return messageTime;
	}
	public void setMessageTime(String messageTime) {
		this.messageTime = messageTime;
	}
	public String getMessageId() {
		return messageId;
	}
	public void setMessageId(String messageId) {
		this.messageId = messageId;
	}
	public String getMessageName() {
		return messageName;
	}
	public void setMessageName(String messageName) {
		this.messageName = messageName;
	}
	public String getMessageSex() {
		return messageSex;
	}
	public void setMessageSex(String messageSex) {
		this.messageSex = messageSex;
	}
	public String getMessageEmail() {
		return messageEmail;
	}
	public void setMessageEmail(String messageEmail) {
		this.messageEmail = messageEmail;
	}
	public String getMessageQQ() {
		return messageQQ;
	}
	public void setMessageQQ(String messageQQ) {
		this.messageQQ = messageQQ;
	}
	public String getMessageTel() {
		return messageTel;
	}
	public void setMessageTel(String messageTel) {
		this.messageTel = messageTel;
	}
	public String getMessageAdd() {
		return messageAdd;
	}
	public void setMessageAdd(String messageAdd) {
		this.messageAdd = messageAdd;
	}
	public String getMessageCont() {
		return messageCont;
	}
	public void setMessageCont(String messageCont) {
		this.messageCont = messageCont;
	}
	
}
